    def extract_linking(ner_model: NER_Model, query: str, threshold=0.6) -> list[str]:
        """
        对 query 进行实体抽取，并根据阈值过滤结果

        Args:
            ner_model: NER_Model 实例
            query: 待查询的文本字符串
            threshold: 相似度/概率阈值，默认为 0.6

        Returns:
            符合阈值条件的实体 span 列表
        """
        # 1. 调用模型获取结果
        raw_result = ner_model.ner(query)

        # 2. 健壮性检查：确保返回结果不为空且包含 output 键
        if not raw_result or 'output' not in raw_result[0]:
            return []

        # 3. 提取实体列表
        entities = raw_result[0]['output']

        # 4. 遍历实体，过滤出 prob >= threshold 的 span
        # 根据你提供的 result 示例，prob 是 float 类型
        filtered_spans = [
            item['span']
            for item in entities
            if item.get('prob', 0) >= threshold
        ]

        return filtered_spans

